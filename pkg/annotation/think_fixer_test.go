// --- fallback ---

type failingThinkSuggester struct{}

func (f *failingThinkSuggester) SuggestThink(_ context.Context, _ ThinkSuggestRequest) (ThinkSuggestion, error) {
	return ThinkSuggestion{}, fmt.Errorf("LLM unavailable")
}
func (f *failingThinkSuggester) ModelName() string { return "failing" }

func TestFallbackThinkSuggester(t *testing.T) {
	fb := &fallbackThinkSuggester{
		primary:  &failingThinkSuggester{},
		fallback: &heuristicThinkSuggester{},
	}

	got, err := fb.SuggestThink(context.Background(), ThinkSuggestRequest{FuncName: "CreateUser", Kind: "func"})
	if err != nil {
		t.Fatal(err)
	}
	if got.Intent != "建立資源" {
		t.Errorf("fallback intent = %q, want 建立資源", got.Intent)
	}
	if fb.ModelName() != "failing" {
		t.Errorf("model name should prefer primary: %q", fb.ModelName())
	}
}
