func TestFixThinkFile_Indentation(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "indent.go")
	src := `package example

// Run 執行
// @ai:generated by=claude-opus-4-6 date=2026-06-18
func Run() {
    println("4-space indent")
}
`
	if err := os.WriteFile(path, []byte(src), 0644); err != nil {
		t.Fatal(err)
	}

	report, _ := CheckThink(path)
	s := &heuristicThinkSuggester{}
	FixThinkFile(context.Background(), path, report, s)

	data, _ := os.ReadFile(path)
	lines := strings.Split(string(data), "\n")
	for _, line := range lines {
		if strings.Contains(line, "@ai:think") {
			if !strings.HasPrefix(line, "    //@ai:think") {
				t.Errorf("indentation mismatch: %q", line)
			}
			break
		}
	}
}

