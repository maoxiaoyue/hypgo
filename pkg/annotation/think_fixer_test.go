// --- NewThinkSuggester rejects mode=none ---

func TestNewThinkSuggester_RejectsNone(t *testing.T) {
	cases := []struct {
		name string
		cfg  *config.LLMConfig
	}{
		{"nil config", nil},
		{"empty mode", &config.LLMConfig{}},
		{"mode=none", &config.LLMConfig{Mode: config.LLMModeNone}},
	}
	for _, tt := range cases {
		s, err := NewThinkSuggester(tt.cfg)
		if err == nil {
			t.Errorf("%s: expected error, got suggester %v", tt.name, s)
		}
		if s != nil {
			t.Errorf("%s: suggester should be nil on error", tt.name)
		}
	}
}

// --- FixThinkFile ---

func TestFixThinkFile_Heuristic(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "fix.go")
	src := `package example

// CreateUser 建立使用者
// @ai:generated by=claude-opus-4-6 date=2026-06-18
func CreateUser() {
	println("no think here")
}

// GetUser 讀取使用者
// @ai:generated by=claude-opus-4-6 date=2026-06-18
func GetUser() {
	println("also missing")
}
`
	if err := os.WriteFile(path, []byte(src), 0644); err != nil {
		t.Fatal(err)
	}

	report, err := CheckThink(path)
	if err != nil {
		t.Fatal(err)
	}
	if len(report.Missing) != 2 {
		t.Fatalf("missing = %d, want 2", len(report.Missing))
	}

	s := &heuristicThinkSuggester{}
	fixed, err := FixThinkFile(context.Background(), path, report, s)
	if err != nil {
		t.Fatal(err)
	}
	if fixed != 2 {
		t.Errorf("fixed = %d, want 2", fixed)
	}

	data, _ := os.ReadFile(path)
	content := string(data)

	if !strings.Contains(content, "//@ai:think intent=建立資源 model=heuristic") {
		t.Error("missing CreateUser @ai:think")
	}
	if !strings.Contains(content, "//@ai:think intent=讀取資料 model=heuristic") {
		t.Error("missing GetUser @ai:think")
	}

	// Verify backup exists
	if _, err := os.Stat(path + ".bak"); err != nil {
		t.Error("backup file not created")
	}
}

