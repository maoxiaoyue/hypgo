func TestCheckFileMethod(t *testing.T) {
	content := `package testpkg

// Server represents a test server.
//
// @ai:generated by=claude date=2026-04-23
type Server struct{}

// Start starts the server.
//
// @ai:generated by=claude date=2026-04-23
// @ai:purpose 啟動伺服器
// @ai:input none
// @ai:output error
// @ai:sideeffect 開啟 listening socket
func (s *Server) Start() error { return nil }

func (s *Server) Stop() {}
`
	tmpFile := writeTempGoFile(t, content)

	report, err := CheckFile(tmpFile)
	if err != nil {
		t.Fatalf("CheckFile failed: %v", err)
	}

	var startResult, stopResult *CheckResult
	for i, r := range report.Results {
		if strings.Contains(r.Name, "Start") {
			startResult = &report.Results[i]
		}
		if strings.Contains(r.Name, "Stop") {
			stopResult = &report.Results[i]
		}
	}
	if startResult == nil || stopResult == nil {
		t.Fatal("methods not found")
	}
	if !startResult.HasRequired {
		t.Errorf("Start should be compliant, missing=%v", startResult.MissingRequired)
	}
	if stopResult.HasRequired {
		t.Error("Stop should not be compliant (no @ai tags)")
	}
	if stopResult.Kind != "method" {
		t.Errorf("Stop.Kind = %q, want method", stopResult.Kind)
	}
}

// --- FormatReport ---

func TestFormatReport(t *testing.T) {
	report := &CheckReport{
		Filename: "test.go",
		Total:    3,
		Passed:   1,
		Results: []CheckResult{
			{Name: "package test", Kind: "package", HasDoc: true, HasRequired: true, Line: 1},
			{Name: "func Create", Kind: "func", Exported: true, HasDoc: false, HasRequired: false, Line: 5,
				MissingRequired: []AnnotationType{Generated, Purpose, Input, Output, SideEffect},
				Suggested:       "// Create ...\n//\n// @ai:generated by=unknown date=2026-04-23"},
			{Name: "type User", Kind: "type", Exported: true, HasDoc: true, HasRequired: false, Line: 10,
				MissingRequired: []AnnotationType{Generated},
				Suggested:       "// User ...\n//\n// @ai:generated by=unknown date=2026-04-23"},
		},
	}

	output := FormatReport(report)
	if !strings.Contains(output, "[PASS]") {
		t.Error("should contain [PASS]")
	}
	if !strings.Contains(output, "[FAIL]") {
		t.Error("should contain [FAIL] for missing doc")
	}
	if !strings.Contains(output, "[WARN]") {
		t.Error("should contain [WARN] for missing @ai tags")
	}
	if !strings.Contains(output, "1/3") {
		t.Error("should contain 1/3 summary")
	}
	if !strings.Contains(output, "--fix") {
		t.Error("should suggest --fix")
	}
}

// --- FixFile ---

func TestFixFileNoDoc(t *testing.T) {
	content := `package testpkg

func ExportedFunc() {}
`
	tmpFile := writeTempGoFile(t, content)

	report, err := CheckFile(tmpFile)
	if err != nil {
		t.Fatalf("CheckFile failed: %v", err)
	}

	if err := FixFile(tmpFile, report.Results); err != nil {
		t.Fatalf("FixFile failed: %v", err)
	}

	if _, err := os.Stat(tmpFile + ".bak"); os.IsNotExist(err) {
		t.Error("backup file should be created")
	}

	fixed, _ := os.ReadFile(tmpFile)
	fixedStr := string(fixed)

	if !strings.Contains(fixedStr, "@ai:generated") {
		t.Error("@ai:generated should be added")
	}
	if !strings.Contains(fixedStr, "@ai:purpose") {
		t.Error("@ai:purpose should be added")
	}
	if !strings.Contains(fixedStr, "@ai:sideeffect") {
		t.Error("@ai:sideeffect should be added")
	}
}

func TestFixFilePartialDoc(t *testing.T) {
	// 既有 doc 但缺 @ai tags → 應該 append 在 doc 後面
	content := `package testpkg

// ExportedFunc does something important.
func ExportedFunc() {}
`
	tmpFile := writeTempGoFile(t, content)

	report, err := CheckFile(tmpFile)
	if err != nil {
		t.Fatalf("CheckFile failed: %v", err)
	}

	if err := FixFile(tmpFile, report.Results); err != nil {
		t.Fatalf("FixFile failed: %v", err)
	}

	fixed, _ := os.ReadFile(tmpFile)
	fixedStr := string(fixed)

	// 原有的 "does something important." 必須保留
	if !strings.Contains(fixedStr, "does something important") {
		t.Error("original doc should be preserved")
	}
	// 補上的 @ai 欄位必須存在
	for _, tag := range []string{"@ai:generated", "@ai:purpose", "@ai:input", "@ai:output", "@ai:sideeffect"} {
		if !strings.Contains(fixedStr, tag) {
			t.Errorf("%s should be appended", tag)
		}
	}
}

