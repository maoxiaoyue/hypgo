// --- CheckFile ---

func TestCheckFileFullyCompliant(t *testing.T) {
	content := `// Package testpkg provides test coverage.
//
// @ai:generated by=claude date=2026-04-23
// @ai:purpose test package
package testpkg

// ExportedFunc does something.
//
// @ai:generated by=claude date=2026-04-23
// @ai:purpose 提供測試用
// @ai:input none
// @ai:output error
// @ai:sideeffect none
func ExportedFunc() error { return nil }

func unexportedFunc() {}
`
	tmpFile := writeTempGoFile(t, content)

	report, err := CheckFile(tmpFile)
	if err != nil {
		t.Fatalf("CheckFile failed: %v", err)
	}

	if report.Total != 2 {
		t.Errorf("Total = %d, want 2 (package + ExportedFunc)", report.Total)
	}
	if report.Passed != 2 {
		t.Errorf("Passed = %d, want 2 (all compliant)", report.Passed)
	}
	for _, r := range report.Results {
		if !r.HasRequired {
			t.Errorf("%s should HasRequired=true, missing=%v", r.Name, r.MissingRequired)
		}
	}
}

func TestCheckFileMissingAITags(t *testing.T) {
	// 有 doc 但缺 @ai 欄位
	content := `package testpkg

// ExportedFunc does something.
func ExportedFunc() {}
`
	tmpFile := writeTempGoFile(t, content)

	report, err := CheckFile(tmpFile)
	if err != nil {
		t.Fatalf("CheckFile failed: %v", err)
	}

	var funcResult *CheckResult
	for i, r := range report.Results {
		if strings.Contains(r.Name, "ExportedFunc") {
			funcResult = &report.Results[i]
		}
	}
	if funcResult == nil {
		t.Fatal("ExportedFunc result not found")
	}
	if !funcResult.HasDoc {
		t.Error("ExportedFunc should HasDoc=true")
	}
	if funcResult.HasRequired {
		t.Error("ExportedFunc should HasRequired=false (missing @ai tags)")
	}
	if len(funcResult.MissingRequired) != 5 {
		t.Errorf("should miss 5 @ai tags, got %d: %v", len(funcResult.MissingRequired), funcResult.MissingRequired)
	}
}

