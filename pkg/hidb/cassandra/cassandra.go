// asDuration 接受 time.Duration、string（"5s"）或 int / int64（奈秒）。
// @ai:generated by=claude-sonnet-4-6 date=2026-04-29
func asDuration(v interface{}) (time.Duration, bool) {
	switch x := v.(type) {
	case time.Duration:
		return x, true
	case string:
		d, err := time.ParseDuration(x)
		return d, err == nil
	case int:
		return time.Duration(x), true
	case int64:
		return time.Duration(x), true
	default:
		return 0, false
	}
}

