// init 初始化 cluster 配置。
// @ai:generated by=claude-sonnet-4-6 date=2026-04-29
func (c *CassandraDB) init() error {
	if len(c.config.Hosts) == 0 {
		return fmt.Errorf("cassandra: at least one host is required")
	}

	cluster := gocql.NewCluster(c.config.Hosts...)

	// Keyspace
	if c.config.Keyspace != "" {
		cluster.Keyspace = c.config.Keyspace
	}

	// Consistency — 嚴格模式下拼錯直接 error，非嚴格 fallback 至 LocalOne
	cl, err := parseConsistencyStrict(c.config.Consistency)
	if err != nil {
		if c.config.StrictConsistency {
			return err
		}
		cl = gocql.LocalOne
	}
	cluster.Consistency = cl

	// Port
	if c.config.Port > 0 {
		cluster.Port = c.config.Port
	}

	// 認證
	if c.config.Username != "" {
		cluster.Authenticator = gocql.PasswordAuthenticator{
			Username: c.config.Username,
			Password: c.config.Password,
		}
	}

	// 超時設置
	if c.config.ConnectTimeout > 0 {
		cluster.ConnectTimeout = c.config.ConnectTimeout
	} else {
		cluster.ConnectTimeout = 5 * time.Second
	}

	if c.config.Timeout > 0 {
		cluster.Timeout = c.config.Timeout
	} else {
		cluster.Timeout = 10 * time.Second
	}

	// 連接池
	if c.config.NumConns > 0 {
		cluster.NumConns = c.config.NumConns
	}

	if c.config.MaxPreparedStmts > 0 {
		cluster.MaxPreparedStmts = c.config.MaxPreparedStmts
	}

	// 協議版本
	if c.config.ProtoVersion > 0 {
		cluster.ProtoVersion = c.config.ProtoVersion
	} else {
		cluster.ProtoVersion = 4
	}

	// Retry policy（NumRetries < 0 表示停用）
	if c.config.NumRetries >= 0 {
		retries := c.config.NumRetries
		if retries == 0 {
			retries = 3
		}
		minD := c.config.RetryMinDelay
		if minD <= 0 {
			minD = 100 * time.Millisecond
		}
		maxD := c.config.RetryMaxDelay
		if maxD <= 0 {
			maxD = 10 * time.Second
		}
		cluster.RetryPolicy = &gocql.ExponentialBackoffRetryPolicy{
			NumRetries: retries,
			Min:        minD,
			Max:        maxD,
		}
	}

	// Reconnection policy
	// ReconnectMax < 0 → 明確停用（覆寫 gocql 預設為 nil）
	// ReconnectMax = 0 → 使用 hidb 預設 (10 retries, 1s interval)
	// ReconnectMax > 0 → 使用使用者指定值
	if c.config.ReconnectMax < 0 {
		cluster.ReconnectionPolicy = nil
	} else {
		maxR := c.config.ReconnectMax
		if maxR == 0 {
			maxR = 10
		}
		intv := c.config.ReconnectInterval
		if intv <= 0 {
			intv = 1 * time.Second
		}
		cluster.ReconnectionPolicy = &gocql.ConstantReconnectionPolicy{
			MaxRetries: maxR,
			Interval:   intv,
		}
	}

	// TLS
	if c.config.TLS.Enabled {
		ssl, err := buildSslOptions(c.config.TLS)
		if err != nil {
			return fmt.Errorf("cassandra: tls config: %w", err)
		}
		cluster.SslOpts = ssl
	}

	// QueryObserver
	if c.config.QueryObserver != nil {
		cluster.QueryObserver = c.config.QueryObserver
	}

	c.cluster = cluster
	return nil
}

// buildSslOptions translates TLSConfig into gocql.SslOptions.
// @ai:generated by=claude-sonnet-4-6 date=2026-04-29
func buildSslOptions(t TLSConfig) (*gocql.SslOptions, error) {
	opts := &gocql.SslOptions{
		EnableHostVerification: !t.InsecureSkipVerify,
	}
	if t.Config != nil {
		opts.Config = t.Config
		return opts, nil
	}
	if t.CertFile != "" && t.KeyFile == "" {
		return nil, fmt.Errorf("cert_file set but key_file is empty")
	}
	opts.CertPath = t.CertFile
	opts.KeyPath = t.KeyFile
	opts.CaPath = t.CaFile
	if t.ServerName != "" {
		opts.Config = &tls.Config{ServerName: t.ServerName}
	}
	return opts, nil
}

// Connect 建立 Cassandra 連接（idempotent）。
//
// @ai:generated by=claude-sonnet-4-6 date=2026-04-29
// @ai:purpose 安全建立或重建 session — 已連線時 no-op，重連時關閉舊 session 不洩漏
// @ai:input none
// @ai:output 連線錯誤或 nil
// @ai:sideeffect 建立 gocql.Session；重連時關閉舊 session
func (c *CassandraDB) Connect() error {
	c.mu.Lock()
	defer c.mu.Unlock()

	if c.closed {
		return fmt.Errorf("cassandra: cannot connect on closed instance")
	}
	if c.cluster == nil {
		return fmt.Errorf("cassandra: cluster config not initialized")
	}
	// 已有有效 session 則 no-op；舊 session 已關閉則先丟棄
	if c.session != nil {
		if !c.session.Closed() {
			return nil
		}
		c.session.Close()
		c.session = nil
	}

	session, err := c.cluster.CreateSession()
	if err != nil {
		return fmt.Errorf("cassandra: failed to create session: %w", err)
	}
	c.session = session
	return nil
}

// Session 獲取 gocql.Session（用戶直接操作 CQL）
func (c *CassandraDB) Session() *gocql.Session {
	c.mu.Lock()
	defer c.mu.Unlock()
	return c.session
}

// Close 關閉 Cassandra 連接（thread-safe，可重複呼叫）。
//
// @ai:generated by=claude-sonnet-4-6 date=2026-04-29
// @ai:purpose 安全關閉 session — 多 goroutine 同時 / 重複呼叫不會 panic 或 double-close
// @ai:input none
// @ai:output nil（保留介面相容）
// @ai:sideeffect 關閉底層 gocql Session
func (c *CassandraDB) Close() error {
	c.mu.Lock()
	defer c.mu.Unlock()
	if c.closed {
		return nil
	}
	if c.session != nil {
		c.session.Close()
		c.session = nil
	}
	c.closed = true
	return nil
}

// Ping 健康檢查。
// @ai:generated by=claude-sonnet-4-6 date=2026-04-29
func (c *CassandraDB) Ping(ctx context.Context) error {
	sess := c.Session()
	if sess == nil {
		return fmt.Errorf("cassandra: session not connected")
	}

	iter := sess.Query("SELECT now() FROM system.local").WithContext(ctx).Iter()
	if err := iter.Close(); err != nil {
		return fmt.Errorf("cassandra: ping failed: %w", err)
	}
	return nil
}

// IsConnected 檢查是否已連接。
// @ai:generated by=claude-sonnet-4-6 date=2026-04-29
func (c *CassandraDB) IsConnected() bool {
	c.mu.Lock()
	defer c.mu.Unlock()
	if c.session == nil {
		return false
	}
	return !c.session.Closed()
}

