// TLSConfig holds optional TLS settings for the CQL connection.
type TLSConfig struct {
	Enabled            bool   `mapstructure:"enabled" yaml:"enabled"`
	CertFile           string `mapstructure:"cert_file" yaml:"cert_file"`
	KeyFile            string `mapstructure:"key_file" yaml:"key_file"`
	CaFile             string `mapstructure:"ca_file" yaml:"ca_file"`
	ServerName         string `mapstructure:"server_name" yaml:"server_name"`
	InsecureSkipVerify bool   `mapstructure:"insecure_skip_verify" yaml:"insecure_skip_verify"`
	// Config, when set, overrides CertFile/KeyFile/CaFile and is passed
	// directly to gocql via SslOptions.Config.
	Config *tls.Config `mapstructure:"-" yaml:"-"`
}

// Config Cassandra 配置
type Config struct {
	Hosts            []string      `mapstructure:"hosts" yaml:"hosts"`
	Keyspace         string        `mapstructure:"keyspace" yaml:"keyspace"`
	Consistency      string        `mapstructure:"consistency" yaml:"consistency"` // "one", "quorum", "all", "local_quorum" 等
	Port             int           `mapstructure:"port" yaml:"port"`               // 預設 9042
	Username         string        `mapstructure:"username" yaml:"username"`
	Password         string        `mapstructure:"password" yaml:"password"`
	ConnectTimeout   time.Duration `mapstructure:"connect_timeout" yaml:"connect_timeout"` // 預設 5s
	Timeout          time.Duration `mapstructure:"timeout" yaml:"timeout"`                 // 預設 10s
	NumConns         int           `mapstructure:"num_conns" yaml:"num_conns"`             // 每個主機的連接數
	MaxPreparedStmts int           `mapstructure:"max_prepared_stmts" yaml:"max_prepared_stmts"`
	ProtoVersion     int           `mapstructure:"proto_version" yaml:"proto_version"` // 預設 4

	// Retry / Reconnect — 預設 production-friendly。設為負值關閉。
	NumRetries        int           `mapstructure:"num_retries" yaml:"num_retries"`               // 預設 3
	RetryMinDelay     time.Duration `mapstructure:"retry_min_delay" yaml:"retry_min_delay"`       // 預設 100ms
	RetryMaxDelay     time.Duration `mapstructure:"retry_max_delay" yaml:"retry_max_delay"`       // 預設 10s
	ReconnectInterval time.Duration `mapstructure:"reconnect_interval" yaml:"reconnect_interval"` // 預設 1s
	ReconnectMax      int           `mapstructure:"reconnect_max" yaml:"reconnect_max"`           // 預設 10

	// TLS 連線設定（dev 環境留空即可）
	TLS TLSConfig `mapstructure:"tls" yaml:"tls"`

	// QueryObserver 注入點，提供 latency / error / pool metrics（gocql QueryObserver）。
	// 注意：此欄位不從 YAML 解析，僅供程式碼直接設定。
	QueryObserver gocql.QueryObserver `mapstructure:"-" yaml:"-"`

	// StrictConsistency=true 時，Consistency 字串無法解析會直接回 error。
	// 預設 false → 無法解析時 fallback 為 LocalOne（並非 Quorum，更利於單節點 dev）。
	StrictConsistency bool `mapstructure:"strict_consistency" yaml:"strict_consistency"`
}

// CassandraDB Cassandra 數據庫管理器
type CassandraDB struct {
	cluster *gocql.ClusterConfig
	session *gocql.Session
	config  Config

	mu     sync.Mutex
	closed bool
}

// New 創建 Cassandra 實例並建立連接。
//
// @ai:generated by=claude-sonnet-4-6 date=2026-04-29
// @ai:purpose 一步完成 cluster 設定 + session 建立，呼叫端拿到即可使用
// @ai:input Config（必填 Hosts；其餘有預設值）
// @ai:output 已連線的 *CassandraDB 或設定 / 連線錯誤
