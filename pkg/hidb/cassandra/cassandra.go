func New(cfg Config) (*CassandraDB, error) {
	c := &CassandraDB{
		config: cfg,
	}

	if err := c.init(); err != nil {
		return nil, err
	}

	if err := c.Connect(); err != nil {
		return nil, err
	}

	return c, nil
}

